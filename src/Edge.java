
public class Edge {
	
	private Vnode start;
	private Vnode end;
	private float weigh;
	
	public Vnode getStart() {
		return start;
	}
	public void setStart(Vnode start) {
		this.start = start;
	}
	public Vnode getEnd() {
		return end;
	}
	public void setEnd(Vnode end) {
		this.end = end;
	}
	public float getWeigh() {
		return weigh;
	}
	public void setWeigh(float weigh) {
		this.weigh = weigh;
	}
	
	
	

}
